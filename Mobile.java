public class Mobile {
	static void makeCall() {
		System.out.println("I am makeCall method!");
	}
	static void sendMessage() {
		System.out.println("I am sendMessage method!");
}
	public static void main(String[] args) {
		System.out.println("Calling makeCall method :");
		makeCall();
		System.out.println("Calling sendMessage method :");
		sendMessage();
	}
	
		
}
